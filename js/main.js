function getRandomIndex(array) {
  const index = Math.floor(Math.random() * array.length);
  return index;
}

function randomIntFromInterval(min, max) {
  // min and max included
  return Math.floor(Math.random() * (max - min + 1) + min);
}

function handleClick() {
  //nume
  let randomNumeIndex = getRandomIndex(nume);
  updateTextContent("nume", nume[randomNumeIndex]);

  //ocupatie
  updateTextContent("ocupatie", ocupatii[randomNumeIndex]);

  //rudenie
  let randomRudenieIndex = getRandomIndex(rudenii);
  updateTextContent("rudenie", rudenii[randomRudenieIndex]);

  //varsta
  let randomVarsta = randomIntFromInterval(2, 18);
  updateTextContent("varsta", randomVarsta);

  //probleme
  let randomProblemaIndex = getRandomIndex(probleme);
  updateTextContent("problema", probleme[randomProblemaIndex]);

  //fantani
  let randomFantanaIndex = getRandomIndex(fantani);
  updateTextContent("fantana", fantani[randomFantanaIndex]);

  //rezolvari
  let randomRezolvareIndex = getRandomIndex(rezolvari);
  updateTextContent("rezolvare", rezolvari[randomRezolvareIndex]);

  //ore
  let randomOra = randomIntFromInterval(2, 19);
  updateTextContent("timp", randomOra);

  //prajituri
  let randomPrajiIndex = getRandomIndex(prajituri);
  updateTextContent("praji", prajituri[randomPrajiIndex]);

  //sarbatori
  let randomSarbatoareIndex = getRandomIndex(sarbatori);
  updateTextContent("sarbatoare", sarbatori[randomSarbatoareIndex]);

  //anotimpuri
  let randomAnotimpIndex = getRandomIndex(anotimpuri);
  updateTextContent("anotimp", anotimpuri[randomAnotimpIndex]);

  //orase
  let randomOrasIndex = getRandomIndex(orase);
  updateTextContent("oras", orase[randomOrasIndex]);

  //parte a corpului
  let randomBodyIndex = getRandomIndex(bodyParts);
  updateTextContent("partea-corpului", bodyParts[randomBodyIndex]);

  //felicitari
  let randomFelicitareIndex = getRandomIndex(felicitari);
  updateTextContent("felicitari", felicitari[randomFelicitareIndex]);

  //comments
  updateTextContent("num-comments", randomIntFromInterval(147, 11648));
}

function updateTextContent(dataTarget, value) {
  const elements = document.querySelectorAll(`[data-target="${dataTarget}"]`);
  elements.forEach((element) => {
    element.innerText = value;
  });
}

function likeButton() {
  let graylike = document.querySelector(".like");
  let likes = document.querySelector(".likes-span");
  if (graylike.src.match("images/gray_like.png")) {
    graylike.src = "images/blue_like.png";
    likes.innerHTML = "500";
  } else {
    graylike.src = "images/gray_like.png";
    likes.innerHTML = "499";
  }
}
